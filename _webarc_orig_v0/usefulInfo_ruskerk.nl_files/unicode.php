<h3>Освящение памятников на могилах протоиерея Павла и протоиерея Григория Красноцветовых</h3>
<p>Памятники на могилах протоиерея Павла и его младшего сына протоиерея Григория Красноцветовых на Никольском кладбище Александро-Невской лавры были освящены 2 октября сего года. Чин освящения надгробных крестов и литию по приснопоминаемым протоиереям совершил наместник Лавры епископ Кронштадтский Назарий.</p><p>Присутствовали братия Лавры, духовенство Казанского кафедрального собора, клирики и прихожане приходов города на Неве, а также родственники и близкие почивших. </p>
<p>Гранитные памятники изготовлены по проекту Соловьевой Э.П. на производстве ГПСК “Возрождение”. <br />На лицевой стороне памятника о. Григорию расположен макет из светлого гранита храма святого и благоверного князя Александра Невского, который его трудами был возведен в центре г. Роттердам (Нидерланды) в 2004 году. Митрофорный протоиерей Григорий являлся его настоятелем до самой своей земной кончины (+ 27.02.2017).</p>
<p>На лицевой стороне памятника о. Павлу расположена бронзовая икона с изображением Казанской Божией Матери. Та самая икона, которая вела отца Павла и его семью на протяжении всего жизненного пути. С июля 1996 года и до самой своей смерти (+ 09.03.2019) отец Павел оставался настоятелем и председателем приходского собрания Казанского собора.</p>
